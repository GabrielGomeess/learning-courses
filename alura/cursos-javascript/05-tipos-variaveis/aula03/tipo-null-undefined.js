let nomeEstudante; //A variavel é declarada, porém não é definida (ou seja, UNDEFINED)
console.log(nomeEstudante);
console.log(typeof nomeEstudante);

console.log(nomeEstudante + 3) //Aqui a saída é NaN, é uma operação matemática mal formada


const professora = 'Ana';
console.log(typeof professora);


let telefoneEstudante = null; //Representa a ausência intecional de um valor (NULL)
console.log(typeof telefoneEstudante)

console.log(telefoneEstudante + 3) //Aqui a saída é 3, pois o NULL se comporta como zero na operação
